print("Konversi Suhu (Celcius ke Fahrenheit)")

#input nilai suhu dalam celcius
C = float(input("Masukkan nilai suhu (Celcius): "))

#menghitung suhu dari Celcius ke Fahrenheit
F = 32 + (C * (9 / 5))

#menampilkan suhu dalam Fahrenheit
print("Fahrenheit: ", F)

